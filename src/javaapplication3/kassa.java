    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       DefaultListModel<String> model = (DefaultListModel<String>) jList2.getModel();
    
    if (model.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Чек пустой!", "Внимание", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Connection conn = null;
    PreparedStatement psOrder = null;
    PreparedStatement psItem = null;
    ResultSet rs = null;

    try {
        conn = DbConnection.getConnection();
        conn.setAutoCommit(false);  

        // 1. Считаем сумму и количество позиций
        double totalSum = 0.0;
        int itemCount = model.getSize();

        for (int i = 0; i < itemCount; i++) {
            String line = model.get(i);
            String[] parts = line.split(" — ");
            if (parts.length != 2) continue;

            String priceStr = parts[1].replace(" ₽", "").replace(",", ".").trim();
            double price = Double.parseDouble(priceStr);
            totalSum += price;
        }

        // 2. Создаём запись в orders_report
        String sqlOrder = "INSERT INTO orders_report (total_sum, items_count, status) VALUES (?, ?, 'в обработке') RETURNING order_id";
        psOrder = conn.prepareStatement(sqlOrder);
        psOrder.setDouble(1, totalSum);
        psOrder.setInt(2, itemCount);
        rs = psOrder.executeQuery();

        int orderId = 0;
        if (rs.next()) {
            orderId = rs.getInt("order_id");
        }

        // 3. Записываем позиции в order_items
        String sqlItem = "INSERT INTO order_items (order_id, item_name, price, quantity) VALUES (?, ?, ?, 1)";
        psItem = conn.prepareStatement(sqlItem);

        for (int i = 0; i < itemCount; i++) {
            String line = model.get(i);
            String[] parts = line.split(" — ");
            if (parts.length != 2) continue;

            String itemName = parts[0].trim();
            String priceStr = parts[1].replace(" ₽", "").replace(",", ".").trim();
            double price = Double.parseDouble(priceStr);

            psItem.setInt(1, orderId);
            psItem.setString(2, itemName);
            psItem.setDouble(3, price);
            psItem.addBatch();
        }

        psItem.executeBatch();

        // 4. Фиксируем изменения
        conn.commit();

        
        JOptionPane.showMessageDialog(this,
            "Чек №" + orderId + " успешно сохранён в отчёте!\n" +
            "Сумма: " + String.format("%,.2f ₽", totalSum) + "\n" +
            "Статус: в обработке",
            "Успех",
            JOptionPane.INFORMATION_MESSAGE);

        // Очищаем текущий заказ
        model.clear();

    } catch (Exception ex) {
        
        try { if (conn != null) conn.rollback(); } catch (Exception ignored) {}
        
        JOptionPane.showMessageDialog(this,
            "Ошибка при сохранении чека:\n" + ex.getMessage(),
            "Ошибка",
            JOptionPane.ERROR_MESSAGE);
        
        ex.printStackTrace();

    } finally {
       
        try { if (rs != null) rs.close(); } catch (Exception ignored) {}
        try { if (psItem != null) psItem.close(); } catch (Exception ignored) {}
        try { if (psOrder != null) psOrder.close(); } catch (Exception ignored) {}
        try { if (conn != null) conn.close(); } catch (Exception ignored) {}
    }

    }//GEN-LAST:event_jButton3ActionPerformed

